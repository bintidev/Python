
# Escribe un programa que recoja la edad del usuario y muestre la edad que
#tendrá dentro de 5, 10 y 15 años.

edad = int(input("Introduce tu edad: "))

print ("Actualmente tienes ",edad," años.",
        "\n\t- Dentro de 5 años tendrás",(edad+5),
        "\n\t- Dentro de 10 años tendrás",(edad+10),
        "\n\t- Dentro de 15 años tendrás",(edad+15),
    )